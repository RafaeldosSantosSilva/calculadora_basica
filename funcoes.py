# Funções para funcionar a calculadora

def soma():
    a = float(input('Digite o primeiro numero: '))
    b = float(input('Digite o segundo numero: '))
    return a + b

def subtracao():
    a = float(input('Digite o primeiro numero: '))
    b = float(input('Digite o segundo numero: '))
    return a - b

def multiplicacao():
    a = float(input('Digite o primeiro numero: '))
    b = float(input('Digite o segundo numero: '))
    return a * b

def divisao():
    a = float(input('Digite o primeiro numero: '))
    b = float(input('Digite o segundo numero: '))
    return a / b